class DataProcessor:
    def __init__(self):
        pass

    def categorize_data(self, df):
        """
        HS 코드를 기반으로 대분류를 할당합니다.
        """
        if df is None or df.empty:
            return df

        def get_category(hs):
            hs_str = str(hs).replace('.', '')
            for cat, codes in ICT_CATEGORIES.items():
                if any(hs_str.startswith(code) for code in codes):
                    return cat
            return "기타 ICT"

        df['category'] = df['hs_code'].apply(get_category)
        return df

    def calculate_growth(self, current_df, prev_df):
        """
        당월 vs 전월 증감률을 계산합니다.
        """
        if current_df is None or prev_df is None:
            return current_df

        # 품목별로 정렬 및 병합
        merged = pd.merge(current_df, prev_df[['hs_code', 'item_name', 'exp_amount']],
                          on=['hs_code', 'item_name'], suffixes=('_curr', '_prev'), how='left')
        merged['growth_amount'] = merged['exp_amount_curr'] - merged['exp_amount_prev'].fillna(0)
        merged['growth_rate'] = (merged['growth_amount'] / merged['exp_amount_prev'] * 100).fillna(0)

        return merged

    def get_time_series_data(self, data_list):
        """
        여러 달의 데이터를 병합하여 시계열 데이터프레임을 생성합니다.
        data_list: List of DataFrames
        """
        if not data_list:
            return pd.DataFrame()

        combined = pd.concat(data_list, ignore_index=True)
        return combined

    def get_service_trade_data(self, months_list):
        """
        한국은행(BOK) ECOS API를 통해 실측 서비스 무역 데이터를 가져옵니다.
        """
        import api_client
        ecos_client = api_client.ECOSAPIClient()
        
        if not months_list:
            return pd.DataFrame()
            
        start_month = months_list[0]
        end_month = months_list[-1]
        
        # 실계측 데이터 호출
        df = ecos_client.fetch_service_trade_data(start_month, end_month)
        
        # API 결과가 없을 경우를 대비한 기본 컬럼 구조 보장
        if df is None or df.empty:
            return pd.DataFrame(columns=['year_month', 'service_name', 'exp_amount', 'imp_amount'])
            
        # 수입액(imp_amount) 데이터가 ECOS 응답에 없을 경우를 대비해 0 또는 비율로 임시 처리
        if 'imp_amount' not in df.columns:
            df['imp_amount'] = df['exp_amount'] * 0.7  # BOP 서비스의 대략적인 수지 시뮬레이션 (실측 시 수입 코드 추가 필요)
            
        return df

